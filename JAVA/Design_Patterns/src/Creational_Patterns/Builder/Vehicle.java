package Creational_Patterns.Builder;

public class Vehicle {
  public String brand;
  public String model;
  public String motor;
  public int doors;

  public String getBrand() {
    return brand;
  }

  public String getModel() {
    return model;
  }

  public String getMotor() {
    return motor;
  }

  public int getDoors() {
    return doors;
  }
}
