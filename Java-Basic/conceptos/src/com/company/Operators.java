package com.company;

public class Operators {
  public static void main(String[] args) {
    //arithmetics
    int number1 = 11;
    int number2 = 2;
    int resultSum= number1 + number2;
    System.out.println(resultSum);
    System.out.println(resultSum + 5.76);

    int resultSubtraction = number1-number2;
    System.out.println(resultSubtraction);

    //comparisons
    // <, > , <=, >=, ==

    boolean resultCompare1 = number1 < number2;
    System.out.println(resultCompare1);

    boolean resultCompare2 = number1 > number2;
    System.out.println(resultCompare2);

    //Logics
    // and &&, or ||

    boolean resultLogical = number1 >5 && number1 < 30;





  }
}
